/**
 * Esta clase contiene los atributos y metodos para implementar un recurso
 * @author Victor Moreno Sola
 * @version 1.0-PCTR
*/
public class recurso 
{
    private long n;

    /**
     * Función que incrementa el valor de n
     */
    public void inc(){n++;}

    /**
     * Función que devuelve el valor de n
     * @return Atributo private de la clase de tipo long
     */
    public long observer(){return n;}    
}
